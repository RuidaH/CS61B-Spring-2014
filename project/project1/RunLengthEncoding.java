  /**
   *  setPixel() modifies this run-length encoding so that the specified color
   *  is stored at the given (x, y) coordinates.  The old pixel value at that
   *  coordinate should be overwritten and all others should remain the same.
   *  The updated run-length encoding should be compressed as much as possible;
   *  there should not be two consecutive runs with exactly the same RGB color.
   *
   *  @param x the x-coordinate of the pixel to modify.
   *  @param y the y-coordinate of the pixel to modify.
   *  @param red the new red intensity to store at coordinate (x, y).
   *  @param green the new green intensity to store at coordinate (x, y).
   *  @param blue the new blue intensity to store at coordinate (x, y).
   */
  public void setPixel(int x, int y, short red, short green, short blue) {
      
      if (x < 0 || y < 0 || x >= width || y >= height) {
          return;
      }
      
      int position = x * height + y + 1;
      int counts = 1, prevCount = 0;
      DListNode prev, next, node = (DListNode)runs.front();
      int[] temp = (int[])node.item(), prevTemp = null, nextTemp, cur = new int[4];
      
      cur[0] = 1;
      cur[1] = red;
      cur[2] = green;
      cur[3] = blue;
      
      while (counts < position && runs.next(node) != null) {
          node = runs.next(node);
          prevTemp = temp;
          temp = (int[])node.item();
          prevCount = counts;
          counts = counts + temp[0];
          
      }
      
      // If they have the same pixel, do nothing
      if (!samePixel(cur, temp)) {
          
          if (counts == position) {
              
              if (runs.next(node) != null) {
                  next = runs.next(node);
                  // Check if the pixel is the same as the next run
                  nextTemp = (int[])next.item();
                  if (samePixel(cur, nextTemp)) {
                      nextTemp[0] = nextTemp[0] + 1;
                      next.setItem(nextTemp);
                  } else {
                      runs.insertAfter(cur, node);
                  }
              } else {
                  runs.insertBack(cur);
              }

          } else if (position == prevCount + 1) {
              
              if (runs.prev(node) != null) {
                  prev = runs.prev(node);
                  // Check if the pixel is the same as the previous run
                  prevTemp = (int[])prev.item();
                  if (samePixel(cur, prevTemp)) {
                      prevTemp[0] = prevTemp[0] + 1;
                      prev.setItem(prevTemp);
                  } else {
                      runs.insertBefore(cur, node);
                  }
              } else {
                  runs.insertFront(cur);
              }

          } else { // counts > position but position didn't reach the end the counts
              
              int oneFrac = counts - position;
              int otherFrac = position - prevCount - 1;
              int[] frac = new int[4];
              
              frac[0] = otherFrac;
              frac[1] = temp[1];
              frac[2] = temp[2];
              frac[3] = temp[3];
              
              temp[0] = oneFrac;
              node.setItem(temp);
              
              runs.insertBefore(frac, node);
              runs.insertBefore(cur, node);

          }
          
          // The original pixel has been altered
          // So the number of pixels in this run deduct 1
          temp[0] = temp[0] - 1;
          node.setItem(temp);
          
          if (temp[0] == 0) {
              node.remove();
          }
          
      }

      check();
  }
  
