  /**
   *  RunLengthEncoding() (with one parameter) is a constructor that creates
   *  a run-length encoding of a specified PixImage.
   * 
   *  Note that you must encode the image in row-major format, i.e., the second
   *  pixel should be (1, 0) and not (0, 1).
   *
   *  @param image is the PixImage to run-length encode.
   */
  public RunLengthEncoding(PixImage image) {
      
      this.width = image.getWidth();
      this.height = image.getHeight();
      runs = new DList();
      
      int number = 0;
      int x1, y1, prevX = 0, prevY = 0;
      int[] run;
      
      for (int x = 0; x < width; x++) {
          for (int y = 0; y < height; y++) {
              x1 = x;
              y1 = y;
              while (image.getRed(x, y) == image.getRed(x1, y1) &&
                     image.getGreen(x, y) == image.getGreen(x1, y1) &&
                     image.getBlue(x, y) == image.getBlue(x1, y1)) {
                  number++;
                  // When it comes to the last pixel
                  if (x1 == width - 1 && y1 == height - 1) {
                      // let prevY jump out of for loop
                      prevY = y1 + 1;
                      break;
                  }
                  
                  prevX = x1;
                  prevY = y1;
                  
                  y1 = (y1 + 1) % height;
                  if (y1 == 0) {
                      x1 = x1 + 1;
                  } else {
                      x1 = x1;
                  }
                
              }
              // Remember to create new Integer Object
              // Or you're just insert the last run into the DList
              run = new int[4];
              run[0] = number;
              run[1] = image.getRed(x, y);
              run[2] = image.getGreen(x, y);
              run[3] = image.getBlue(x, y);
              runs.insertBack(run);
              
              number = 0;
              x = prevX;
              y = prevY;
  
          }
      }

      check();
  }

